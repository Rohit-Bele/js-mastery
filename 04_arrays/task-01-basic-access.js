// Task 01: Basic Access & Modify in Array

const fruits = ["apple", "banana", "mango", "kiwi", "pineapple"];

// Print first element
console.log("First fruit:", fruits[0]);

// Print last element
const lastIndex = fruits.length - 1;
console.log("Last fruit:", fruits[lastIndex]);

// Replace second fruit
fruits[1] = "coconut";

// Print updated array length
console.log("Array length:", fruits.length);

// Print updated array
console.log("Updated fruits:", fruits);